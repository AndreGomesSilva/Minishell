#include "../include/libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	index;
	size_t			len;

	index = -1;
	len = ft_strlen(s);
	while (index++, index < len)
		f(index, (s + index));
}
